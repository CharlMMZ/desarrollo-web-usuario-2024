

function actuaContador(valor) {
    document.parseInt(document.querySelector("div p")).innerText = value;
  }

function sumar() {
    let dedo = parseInt(document.querySelector("div p")).innerText;
    dedo++;
    actuaContador(dedo);
    
}
function restar() {
    let dedo = parseInt(document.querySelector("div p")).innerText;
    dedo--;
    actuaContador(dedo);

}
